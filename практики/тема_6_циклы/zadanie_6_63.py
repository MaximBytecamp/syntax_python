# ==============================================================
# Задание 6.63
# Гражданин 1 марта открыл счёт, вложив 1000 руб.
# Каждый месяц вклад увеличивается на 2%.
#
# а) Прирост суммы вклада за 1-й, 2-й, ..., 10-й месяц.
# б) Сумма вклада через 3, 4, ..., 12 месяцев.
# ==============================================================

initial = 1000.0    # начальная сумма вклада
rate    = 0.02      # процентная ставка за месяц (2% = 0.02)

# ==============================================================
# а) Прирост за каждый из первых 10 месяцев
# ==============================================================
# Прирост за месяц = текущая_сумма * rate
# После начисления: сумма = сумма * (1 + rate)

print("а) Прирост суммы вклада по месяцам:")
print(f"{'Месяц':>6} | {'Сумма до':>12} | {'Прирост':>10} | {'Сумма после':>12}")
print("-" * 48)

amount = initial    # текущая сумма — начинаем с начальной

for month in range(1, 11):      # месяцы 1..10
    growth = amount * rate      # прирост = 2% от текущей суммы
    amount += growth            # новая сумма = старая + прирост

    # amount - growth — сумма ДО начисления; amount — ПОСЛЕ
    print(f"  {month:4d}  | {amount - growth:12.2f} | {growth:10.2f} | {amount:12.2f}")

# ==============================================================
# б) Сумма вклада через 3, 4, ..., 12 месяцев
# ==============================================================
# Пересчитываем с нуля, чтобы не зависеть от предыдущего цикла

print("\nб) Сумма вклада через N месяцев:")
print(f"{'Месяцев':>8} | {'Сумма (руб.)':>14}")
print("-" * 26)

amount = initial

for month in range(1, 13):      # накапливаем месяц за месяцем
    amount = amount * (1 + rate)    # формула сложных процентов за 1 период

    # Выводим только начиная с 3-го месяца
    if month >= 3:
        print(f"    {month:4d}   | {amount:14.2f}")
