# ==============================================================
# Задание 6.13
# Напечатать «столбиком» значения sin 2°, sin 3°, ..., sin 20°
# math.radians() переводит градусы в радианы (нужно для math.sin)
# ==============================================================

print("\nsin от 2° до 20°:")
for deg in range(2, 21):
    rad = math.radians(deg)      # переводим градусы в радианы
    print(f"sin({deg:2d}°) = {math.sin(rad):.6f}")


# ==============================================================
# Задание 6.16
# Вывести «столбиком» значения sin 0.1, sin 0.2, ..., sin 1.1
# (аргументы — в радианах)
# ==============================================================

print("\nsin(x) для x = 0.1, 0.2, ..., 1.1 (радианы):")
# Используем целый счётчик i = 1..11, а x = i / 10.0 чтобы избежать
# погрешностей при сложении вещественных чисел в цикле
for i in range(1, 12):
    x = i / 10.0
    print(f"sin({x:.1f}) = {math.sin(x):.6f}")
