import math

# ==============================================================
# Задание 6.7
# Одна штука товара стоит 20.4 руб.
# Напечатать таблицу стоимости 2, 3, ..., 20 штук.
# ==============================================================

price_per_unit = 20.4

print("Кол-во | Стоимость (руб.)")
print("-" * 25)
for qty in range(2, 21):          # от 2 до 20 включительно
    total = qty * price_per_unit
    print(f"  {qty:2d}   |  {total:.2f}")  # :2d — целое шириной 2; :.2f — 2 знака после точки


# ==============================================================
# Задание 6.8
# Таблица: фунты → килограммы (1 фунт = 453 г = 0.453 кг)
# Для значений 1, 2, ..., 10 фунтов.
