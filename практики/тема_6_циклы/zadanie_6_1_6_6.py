# Задание 6.1
# Напечатать ряд чисел 20 в виде: 20 20 20 20 20 20 20 20 20 20
# ==============================================================

# end=" " — не переходить на новую строку, а ставить пробел
# После цикла — print() для перевода строки
for i in range(10):       # range(10) даёт 0,1,...,9 — всего 10 итераций
    print(20, end=" ")
print()                   # переход на новую строку


