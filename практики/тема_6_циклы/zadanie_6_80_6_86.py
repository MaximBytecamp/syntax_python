# ==============================================================
# Задание 6.80
# Определить, какая цифра встречается чаще: 0 или 9.
# ==============================================================

number = int(input("6.80. Введите натуральное число: "))

count_0 = 0
count_9 = 0

n = number
while n > 0:
    d = n % 10
    n //= 10
    if d == 0:
        count_0 += 1
    if d == 9:
        count_9 += 1

print(f"Цифра 0 встречается {count_0} раз, цифра 9 — {count_9} раз")

if count_0 > count_9:
    print("Цифра 0 встречается чаще")
elif count_9 > count_0:
    print("Цифра 9 встречается чаще")
else:
    print("Цифры 0 и 9 встречаются одинаково часто")


