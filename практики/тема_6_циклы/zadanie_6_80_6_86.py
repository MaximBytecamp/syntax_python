# ==============================================================
# Задание 6.84
# Все цифры числа различны. Какая цифра расположена ЛЕВЕЕ:
# максимальная или минимальная?
# ==============================================================

print()
number = int(input("6.84. Введите натуральное число (все цифры различны): "))

# Извлекаем цифры в список (справа налево)
digits = []
n = number
while n > 0:
    digits.append(n % 10)
    n //= 10

# digits[0] — самая правая, digits[-1] — самая левая
# Позиция в digits: бо́льший индекс = левее в числе
max_digit = max(digits)
min_digit = min(digits)

pos_max = digits.index(max_digit)   # индекс максимальной цифры
pos_min = digits.index(min_digit)   # индекс минимальной цифры

# Бо́льший индекс в списке = левее в числе
print(f"Максимальная цифра: {max_digit} (позиция в списке: {pos_max})")
print(f"Минимальная цифра:  {min_digit} (позиция в списке: {pos_min})")

if pos_max > pos_min:
    print(f"Максимальная цифра ({max_digit}) расположена левее")
else:
    print(f"Минимальная цифра ({min_digit}) расположена левее")


# ==============================================================
# Задание 6.85
# Если в числе есть цифры 2 и 5, определить, какая из них
# расположена ЛЕВЕЕ (учитываем самые левые вхождения каждой).
# ==============================================================

print()
number = int(input("6.85. Введите натуральное число: "))

# Извлекаем цифры справа налево и запоминаем позиции 2 и 5.
# Поскольку извлечение идёт справа налево, КАЖДОЕ НОВОЕ нахождение
# цифры 2 или 5 будет ЛЕВЕЕ предыдущего → обновляем позицию каждый раз.
pos_2 = -1      # позиция цифры 2 в extracted-списке (-1 = не найдена)
pos_5 = -1      # позиция цифры 5 в extracted-списке

n = number
idx = 0         # счётчик позиции в извлечённом потоке (0 = самая правая)
while n > 0:
    d = n % 10
    n //= 10
    if d == 2:
        pos_2 = idx     # обновляем — каждая новая находка левее
    if d == 5:
        pos_5 = idx
    idx += 1

if pos_2 == -1 and pos_5 == -1:
    print("В числе нет ни цифры 2, ни цифры 5")
elif pos_2 == -1:
    print("В числе нет цифры 2")
elif pos_5 == -1:
    print("В числе нет цифры 5")
else:
    # Больший индекс → левее в числе
    if pos_2 > pos_5:
        print(f"Цифра 2 расположена левее цифры 5 (позиции в потоке: 2→{pos_2}, 5→{pos_5})")
    else:
        print(f"Цифра 5 расположена левее цифры 2 (позиции в потоке: 5→{pos_5}, 2→{pos_2})")


# ==============================================================
# Задание 6.86
# Если в числе есть цифры a и b, определить, какая из них
# расположена ПРАВЕЕ (учитываем самые правые вхождения).
# ==============================================================

print()
number = int(input("6.86. Введите натуральное число: "))
a = int(input("  Введите a: "))
b = int(input("  Введите b: "))

# При извлечении справа налево: ПЕРВОЕ вхождение (наименьший индекс)
# соответствует самой ПРАВОЙ позиции в числе.
# Для «самого правого» нам нужно только первое найденное — поэтому
# запоминаем позицию только если ещё не нашли.

pos_a = -1
pos_b = -1

n = number
idx = 0
while n > 0:
    d = n % 10
    n //= 10
    if d == a and pos_a == -1:  # первое нахождение = самое правое
        pos_a = idx
    if d == b and pos_b == -1:
        pos_b = idx
    idx += 1

if pos_a == -1 and pos_b == -1:
    print(f"В числе нет ни цифры {a}, ни цифры {b}")
elif pos_a == -1:
    print(f"В числе нет цифры {a}")
elif pos_b == -1:
    print(f"В числе нет цифры {b}")
else:
    # Меньший индекс → правее в числе
    if pos_a < pos_b:
        print(f"Цифра {a} расположена правее цифры {b} (позиции: {a}→{pos_a}, {b}→{pos_b})")
    elif pos_b < pos_a:
        print(f"Цифра {b} расположена правее цифры {a} (позиции: {b}→{pos_b}, {a}→{pos_a})")
    else:
        print(f"Цифры {a} и {b} — одна и та же цифра, позиция {pos_a}")
