# Задание 6.52
# Является ли натуральное число простым?
# Простое > 1, без делителей кроме 1 и самого себя.
# Оператор цикла с параметром (for) не использовать — только while.
# ==============================================================

print()
number = int(input("6.52. Введите натуральное число: "))

if number < 2:
    print(f"{number} не является простым (простые числа > 1)")
else:
    d = 2
    is_prime = True
    # Проверяем делители от 2 до √number (если d² > number — все делители проверены)
    while d * d <= number:
        if number % d == 0:     # нашли делитель — число составное
            is_prime = False
            break
        d += 1

    print(f"{number} {'является' if is_prime else 'не является'} простым числом")


