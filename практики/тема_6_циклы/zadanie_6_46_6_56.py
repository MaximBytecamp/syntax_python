# Задание 6.53
# Является ли последовательность цифр СПРАВА НАЛЕВО
# строго возрастающей?
# Пример: 5321 → справа налево: 1,2,3,5 → возрастает → ДА
#         7820 → справа налево: 0,2,8,7 → НЕ возрастает (7<8) → НЕТ
# ==============================================================

print()
number = int(input("6.53. Введите натуральное число: "))

# Извлекаем цифры справа налево и проверяем возрастание на ходу
# Каждая следующая извлечённая цифра должна быть БОЛЬШЕ предыдущей

n = number
prev = n % 10           # первая извлечённая (самая правая)
n //= 10
is_ascending = True

while n > 0:
    curr = n % 10       # следующая цифра (идём правее→левее)
    n //= 10
    if curr <= prev:    # должно быть строго больше
        is_ascending = False
    prev = curr

print(f"Цифры {number} справа налево строго возрастают: {is_ascending}")


