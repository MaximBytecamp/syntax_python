# Задание 6.55
# Последовательность цифр СЛЕВА НАПРАВО строго возрастает?
# Пример: 1478 → слева направо: 1,4,7,8 → ДА
#         1782 → 1,7,8,2 → 8>2 нарушение → НЕТ
# ==============================================================

print()
number = int(input("6.55. Введите натуральное число: "))

# Цифры извлекаются справа налево: чтобы проверить порядок слева направо,
# собираем цифры в список, потом проверяем список.
digits = []
n = number
while n > 0:
    digits.append(n % 10)
    n //= 10
# digits = [последняя, ..., первая], т.е. порядок справа налево
# Слева направо — это digits в ОБРАТНОМ порядке: digits[-1], digits[-2], ...
# Строгое возрастание слева направо ↔ строгое убывание справа налево (в digits)

is_ascending_lr = True
i = len(digits) - 1         # начинаем с первой (левой) цифры
while i > 0:
    if digits[i] >= digits[i - 1]:  # слева направо: должно возрастать
        is_ascending_lr = False
    i -= 1

print(f"Цифры {number} слева направо строго возрастают: {is_ascending_lr}")


