# Задание 6.46
# Дано натуральное число. Верно ли, что...
# ==============================================================

number = int(input("6.46. Введите натуральное число: "))

# Извлекаем нужные характеристики за один проход
digit_sum     = 0
digit_product = 1
digit_count   = 0
last_digit    = number % 10       # последняя цифра — до цикла
first_digit   = 0                 # заполним в цикле (последнее извлечённое)

n = number
while n > 0:
    d = n % 10          # очередная цифра (справа налево)
    n //= 10
    digit_sum     += d
    digit_product *= d
    digit_count   += 1
    first_digit    = d  # после последней итерации здесь будет крайняя левая цифра

print(f"\nЧисло: {number}")
print(f"Цифр: {digit_count}, сумма цифр: {digit_sum}, произведение цифр: {digit_product}")
print(f"Первая цифра: {first_digit}, последняя цифра: {last_digit}")

# а) Сумма цифр > 10
print(f"\nа) Сумма цифр ({digit_sum}) > 10: {digit_sum > 10}")

# б) Произведение цифр < 50
print(f"б) Произведение цифр ({digit_product}) < 50: {digit_product < 50}")

# в) Количество цифр чётное
print(f"в) Количество цифр ({digit_count}) чётное: {digit_count % 2 == 0}")

# г) Четырёхзначное число — БЕЗ составного условия и вложенного if.
# Техника: число четырёхзначное, если количество его цифр == 4.
# Это простое сравнение, без and/or и без if внутри if.
print(f"г) Четырёхзначное (количество цифр == 4): {digit_count == 4}")

# д) Первая цифра не превышает 6
print(f"д) Первая цифра ({first_digit}) <= 6: {first_digit <= 6}")

# е) Начинается и заканчивается одинаковой цифрой
print(f"е) first_digit ({first_digit}) == last_digit ({last_digit}): {first_digit == last_digit}")

# ж) Какая из цифр больше: первая или последняя?
if first_digit > last_digit:
    print(f"ж) Первая цифра ({first_digit}) больше последней ({last_digit})")
elif last_digit > first_digit:
    print(f"ж) Последняя цифра ({last_digit}) больше первой ({first_digit})")
else:
    print(f"ж) Первая и последняя цифры равны ({first_digit})")


