# Собираем цифры в список (извлечение идёт справа налево)
digits = []
n = number
while n > 0:
    digits.append(n % 10)
    n //= 10

# digits = [последняя, ..., первая]
# Палиндром: digits читается одинаково слева направо и справа налево
# (т.е. список симметричен)
is_palindrome = digits == digits[::-1]

print(f"Число {number} {'является' if is_palindrome else 'не является'} палиндромом")
print(f"Цифры справа налево: {digits}")


