# ==============================================================
# Задание 6.35
# Даны числа a1, a2, ..., an. Определить их сумму.
# (n вводится с клавиатуры, затем вводятся n чисел)
# ==============================================================

n = int(input("6.35. Введите количество чисел n: "))
total = 0
for i in range(1, n + 1):
    x = float(input(f"  Введите число a{i}: "))
    total += x          # накапливаем сумму
print(f"Сумма {n} чисел = {total}\n")


# ==============================================================
# Задание 6.36
# Даны натуральное число n и вещественные числа a1, ..., an.
# Определить сумму всех вещественных чисел.
# (аналог 6.35, акцент на вещественный тип)
# ==============================================================

n = int(input("6.36. Введите количество чисел n: "))
total = 0.0             # инициализируем как вещественное
