# ==============================================================
# Задание 6.41
# Известно сопротивление каждого элемента цепи.
# Все элементы соединены параллельно.
# Определить общее сопротивление.
# Формула: 1/R = 1/R1 + 1/R2 + ... + 1/Rn → R = 1 / (сумма обратных)
# ==============================================================

n = int(input("6.41. Введите количество элементов цепи: "))
sum_inverse = 0.0           # сумма величин 1/Ri
for i in range(1, n + 1):
    r = float(input(f"  Сопротивление R{i} (Ом): "))
    sum_inverse += 1 / r    # 1/Ri прибавляем к сумме
total_resistance = 1 / sum_inverse
print(f"Общее сопротивление (параллельно): {total_resistance:.4f} Ом\n")


# ==============================================================
# Задание 6.42
# Даны числа a1, a2, ..., a6. Определить их произведение.
# ==============================================================

print("6.42. Введите 6 чисел:")
product = 1.0               # начальное значение накопителя-произведения = 1
for i in range(1, 7):
    x = float(input(f"  a{i} = "))
    product *= x            # умножаем накопитель на очередное число
print(f"Произведение 6 чисел = {product:.4f}\n")


# ==============================================================
# Задание 6.43
# Даны числа a1, a2, ..., a10. Определить сумму их квадратов.
# ==============================================================

print("6.43. Введите 10 чисел:")
sum_of_squares = 0.0
for i in range(1, 11):
    x = float(input(f"  a{i} = "))
    sum_of_squares += x ** 2    # прибавляем квадрат текущего числа
print(f"Сумма квадратов 10 чисел = {sum_of_squares:.4f}")
