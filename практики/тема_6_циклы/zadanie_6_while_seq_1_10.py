# ==============================================================
# Задание 6.1
# Непустая последовательность целых чисел, оканчивается нулём.
# а) Сумма всех чисел; б) Количество всех чисел.
# ==============================================================

print("6.1. Вводите числа по одному. 0 — признак конца.")

total = 0
count = 0

x = int(input("  Число: "))
while x != 0:               # пока не встретили 0 — продолжаем
    total += x              # накапливаем сумму
    count += 1              # считаем количество
    x = int(input("  Число: "))

print(f"а) Сумма = {total}")
print(f"б) Количество чисел = {count}")


# ==============================================================
# Задание 6.2
# Последовательность неотрицательных чисел, оканчивается
# отрицательным. Найти среднее арифметическое.
# ==============================================================

print("\n6.2. Вводите неотрицательные числа. Отрицательное — конец.")

total = 0
count = 0

x = int(input("  Число: "))
while x >= 0:               # пока число неотрицательное — включаем в подсчёт
    total += x
    count += 1
    x = int(input("  Число: "))

mean = total / count if count > 0 else 0
print(f"Среднее арифметическое = {mean:.4f}")


# ==============================================================
# Задание 6.3
# n целых чисел, первое — нечётное. Найти сумму подряд идущих
# нечётных чисел в начале последовательности.
# Условный оператор (if) не использовать.
# ==============================================================

print("\n6.3. Сумма начальных нечётных чисел.")
n = int(input("  Введите n: "))

# Собираем все n чисел
nums = []
for _ in range(n):
    nums.append(int(input("  Число: ")))

# Ключевая идея: while-условие заменяет if.
# Цикл сам прекращается, когда встречает чётное число.
total = 0
i = 0
while nums[i] % 2 != 0:    # пока текущее число нечётное — суммируем
    total += nums[i]
    i += 1
    # Дополнительно: защита от выхода за границу (все числа нечётные)
    # используем составное условие в while — это не "if"
    if i >= n:
        break               # все n чисел нечётные

print(f"Сумма начальных нечётных чисел = {total}")


# ==============================================================
# Задание 6.4
# n вещественных чисел, начинается с отрицательного.
# Определить количество отрицательных чисел подряд в начале.
# Условный оператор не использовать.
# ==============================================================

print("\n6.4. Количество отрицательных чисел в начале.")
n = int(input("  Введите n: "))

nums = []
for _ in range(n):
    nums.append(float(input("  Число: ")))

count = 0
# while-условие: пока число отрицательное — считаем (без if)
while count < n and nums[count] < 0:
    count += 1

print(f"Количество отрицательных в начале = {count}")


# ==============================================================
# Задание 6.5
# 18 чисел, в начале несколько равных. Определить их количество.
# Условный оператор не использовать.
# ==============================================================

print("\n6.5. Количество равных чисел в начале последовательности из 18.")

nums = []
for _ in range(18):
    nums.append(int(input("  Число: ")))

first = nums[0]             # первый элемент — образец для сравнения
count = 1                   # сам первый элемент уже считаем
# while: пока элемент совпадает с первым — двигаемся дальше (без if)
while count < 18 and nums[count] == first:
    count += 1

print(f"Количество равных элементов в начале = {count}")


# ==============================================================
# Задание 6.6
# 15 чисел, отсортированных по возрастанию; задано число n
# (a1 < n < a15, n не совпадает ни с одним элементом).
# а) Сумма элементов, меньших n.
# б) Найти два элемента, между которыми находится n.
# Условный оператор не использовать.
# ==============================================================

print("\n6.6. Отсортированная последовательность из 15 чисел.")

nums = []
for _ in range(15):
    nums.append(float(input("  Число: ")))

n = float(input("  Введите n: "))

# а) Сумма элементов < n (while — пока элемент меньше n, без if)
total = 0.0
i = 0
while nums[i] < n:          # цикл останавливается на первом элементе >= n
    total += nums[i]
    i += 1

print(f"а) Сумма элементов, меньших {n} = {total}")

# б) Два соседних элемента, между которыми n
# После предыдущего цикла: nums[i-1] < n < nums[i]
left_idx  = i - 1           # индекс левого соседа
right_idx = i               # индекс правого соседа
print(f"б) n={n} находится между a[{left_idx+1}]={nums[left_idx]} и a[{right_idx+1}]={nums[right_idx]}")


