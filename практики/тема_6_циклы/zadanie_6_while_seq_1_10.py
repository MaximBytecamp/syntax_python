# Задание 6.7
# Последовательность положительных чисел, оканчивается 0.
# Вывести: a1, a1*a2, a1*a2*a3, ..., 0.
# ==============================================================

print("\n6.7. Последовательные произведения. 0 — конец.")

product = 1
x = int(input("  Число: "))
while x != 0:
    product *= x                        # накапливаем произведение
    print(f"  Произведение = {product}")
    x = int(input("  Число: "))

print(0)                                # завершаем нулём, как условие задачи


# ==============================================================
# Задание 6.8
# Из чисел 1, 4, 9, 16, 25, ... напечатать те, которые <= n.
# ==============================================================

print("\n6.8. Квадраты, не превышающие n.")
n = int(input("  Введите n: "))

k = 1                   # натуральное число, квадрат которого проверяем
while k * k <= n:       # пока k² не превысило n — выводим
    print(k * k, end=" ")
    k += 1
print()


