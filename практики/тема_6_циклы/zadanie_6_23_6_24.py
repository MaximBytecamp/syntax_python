# Задание 6.23
# Найти:
# а) сумму всех целых чисел от 100 до 500;
# б) сумму от a до 500 (a вводится с клавиатуры; a <= 500);
# в) сумму от –10 до b (b вводится с клавиатуры; b >= –10);
# г) сумму от a до b (a и b вводятся с клавиатуры; b >= a).
# ==============================================================

# а) Сумма от 100 до 500
total = 0                       # начальное значение накопителя
for i in range(100, 501):       # 501 — чтобы 500 вошло в диапазон
    total += i                  # total = total + i

print(f"а) Сумма от 100 до 500 = {total}")

# Проверка формулой арифметической прогрессии: S = n*(a1+an)/2
# n = 500-100+1 = 401; S = 401*(100+500)/2 = 120300
print(f"   Проверка (формула): {401 * (100 + 500) // 2}")

# б) Сумма от a до 500
a = int(input("\nВведите a (a <= 500): "))
total = 0
for i in range(a, 501):
    total += i
print(f"б) Сумма от {a} до 500 = {total}")

# в) Сумма от –10 до b
b = int(input("\nВведите b (b >= -10): "))
total = 0
for i in range(-10, b + 1):
    total += i
print(f"в) Сумма от -10 до {b} = {total}")

# г) Сумма от a до b
a = int(input("\nВведите a: "))
b = int(input("Введите b (b >= a): "))
total = 0
for i in range(a, b + 1):
    total += i
print(f"г) Сумма от {a} до {b} = {total}")


