# Задание 6.24
# Найти:
# а) произведение всех целых чисел от 8 до 15;
# б) произведение от a до 20 (a вводится; 1 <= a <= 20);
# в) произведение от 1 до b (b вводится; 1 <= b <= 20);
# г) произведение от a до b (a, b вводятся; b >= a).
# ==============================================================

# а) Произведение от 8 до 15
product = 1                     # начальное значение накопителя — 1 (НЕ 0!)
for i in range(8, 16):
    product *= i                # product = product * i

print(f"\nа) Произведение от 8 до 15 = {product}")

# б) Произведение от a до 20
a = int(input("\nВведите a (1 <= a <= 20): "))
product = 1
for i in range(a, 21):
    product *= i
print(f"б) Произведение от {a} до 20 = {product}")

# в) Произведение от 1 до b (факториал b)
b = int(input("\nВведите b (1 <= b <= 20): "))
product = 1
for i in range(1, b + 1):
    product *= i
print(f"в) Произведение от 1 до {b} = {product}  (это {b}!)")

# г) Произведение от a до b
a = int(input("\nВведите a: "))
b = int(input("Введите b (b >= a): "))
product = 1
for i in range(a, b + 1):
    product *= i
print(f"г) Произведение от {a} до {b} = {product}")
