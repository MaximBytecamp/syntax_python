# Задание 4.50
# Даны три различных целых числа. Определить:
# а) самое большое;
# б) самое маленькое;
# в) среднее (больше наименьшего, но меньше наибольшего).
# ==============================================================

a = 15
b = 7
c = 42

print(f"Числа: a = {a}, b = {b}, c = {c}")

# а) Самое большое
if a >= b and a >= c:
    biggest = a
    label_big = "первое (a)"
elif b >= a and b >= c:
    biggest = b
    label_big = "второе (b)"
else:
    biggest = c
    label_big = "третье (c)"

print(f"а) Самое большое — {label_big} = {biggest}")

# б) Самое маленькое
if a <= b and a <= c:
    smallest = a
    label_small = "первое (a)"
elif b <= a and b <= c:
    smallest = b
    label_small = "второе (b)"
else:
    smallest = c
    label_small = "третье (c)"

print(f"б) Самое маленькое — {label_small} = {smallest}")

# в) Среднее — то, которое не является ни наибольшим, ни наименьшим
# smallest < среднее < biggest
if smallest < a < biggest:
    print(f"в) Среднее — первое (a) = {a}")
elif smallest < b < biggest:
    print(f"в) Среднее — второе (b) = {b}")
else:
    print(f"в) Среднее — третье (c) = {c}")


# ==============================================================
# Задание 4.53
# По номеру месяца (1..12) вывести количество дней в нём.
# Случай 1: год не високосный.
# Случай 2: год может быть високосным.
# Год является високосным, если:
#   делится на 4, НО если делится на 100 — то должен делиться и на 400
# ==============================================================

print()
month = 2  # номер месяца (февраль)
year  = 2024  # год

# --- Случай 1: год заведомо не високосный ---
print("--- Случай 1: не високосный год ---")
print(f"Месяц: {month}")

# Месяцы с 31 днём: 1(янв), 3(мар), 5(май), 7(июл), 8(авг), 10(окт), 12(дек)
# Месяцы с 30 днями: 4(апр), 6(июн), 9(сен), 11(ноя)
# Февраль в обычном году: 28 дней
if month in (1, 3, 5, 7, 8, 10, 12):
    days = 31
elif month in (4, 6, 9, 11):
    days = 30
elif month == 2:
    days = 28
else:
    days = -1  # некорректный ввод

if days != -1:
    print(f"Дней в месяце {month} (не високосный): {days}")
else:
    print("Некорректный номер месяца")

# --- Случай 2: учитываем високосность ---
print(f"\n--- Случай 2: год {year} ---")

# Проверяем, является ли год високосным
# Правило: кратно 4, НО если кратно 100 — должно быть кратно и 400
is_leap = (year % 4 == 0 and year % 100 != 0) or (year % 400 == 0)

print(f"Год {year} {'является' if is_leap else 'не является'} високосным")

if month in (1, 3, 5, 7, 8, 10, 12):
    days = 31
elif month in (4, 6, 9, 11):
    days = 30
elif month == 2:
    # В високосный год в феврале 29 дней
    days = 29 if is_leap else 28
else:
    days = -1

if days != -1:
    print(f"Дней в месяце {month} ({year} год): {days}")
else:
    print("Некорректный номер месяца")
