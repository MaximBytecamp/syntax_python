# Задание 4.5
# Известны год и номер месяца рождения человека, а также год и
# номер месяца сегодняшнего дня. Определить возраст (полных лет).
# При совпадении месяцев считать, что год прошёл полностью.
# ==============================================================

birth_year  = 2000
birth_month = 4   # апрель

today_year  = 2026
today_month = 4   # апрель

# Базовый возраст — разность лет
age = today_year - birth_year

# Если месяц рождения ещё не наступил в этом году — полный год ещё не прошёл
# (при совпадении месяцев считаем, что год прошёл, поэтому только строгое >)
if birth_month > today_month:
    age -= 1

print(f"Дата рождения: {birth_month}.{birth_year}")
print(f"Сегодня:       {today_month}.{today_year}")
print(f"Полных лет:    {age}")


