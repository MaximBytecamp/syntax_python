# Задание 4.38
# Даны два числа. Если √b < a, то увеличить b в пять раз.
# ==============================================================

print()
a = 10
b = 64

print(f"Исходные: a = {a}, b = {b}")
print(f"√b = √{b} = {math.sqrt(b):.4f}")

if math.sqrt(b) < a:
    b = b * 5
    print(f"√b < a, поэтому b увеличивается в 5 раз → b = {b}")
else:
    print(f"√b >= a, b остаётся прежним → b = {b}")


