import math

# ==============================================================
# Задание 4.37
# Уменьшить первое число в два раза, если оно больше второго
# по абсолютной величине (по модулю).
# ==============================================================

a = 10
b = -3

print(f"Исходные: a = {a}, b = {b}")
print(f"Модули: |a| = {abs(a)}, |b| = {abs(b)}")

# abs() возвращает абсолютную величину (модуль) числа
if abs(a) > abs(b):
    a = a / 2  # уменьшаем в два раза
    print(f"|a| > |b|, поэтому a уменьшается вдвое → a = {a}")
else:
    print(f"|a| не больше |b|, a остаётся прежним → a = {a}")


