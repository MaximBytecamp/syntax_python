# Записать условие, которое является истинным, когда:
# ==============================================================

A = 150
B = 42
C = 99

print(f"A = {A}, B = {B}, C = {C}")
print()

# а) Каждое из чисел А и В больше 100
# Оба должны быть > 100, значит используем «и» (and)
# A=150 > 100 → True; B=42 > 100 → False; True and False = False
result_a = A > 100 and B > 100
print(f"а) A > 100 and B > 100  →  {A} > 100 and {B} > 100  →  {A > 100} and {B > 100}  =  {result_a}")

# б) Только одно из чисел А и В чётное
# «Только одно» — это операция исключающего ИЛИ (XOR): ^ или (p or q) and not (p and q)
# Чётность проверяется через остаток от деления на 2: n % 2 == 0
# A=150: 150 % 2 = 0 → чётное True; B=42: 42 % 2 = 0 → чётное True
# True XOR True = False (оба чётные — условие не выполнено)
result_b = (A % 2 == 0) ^ (B % 2 == 0)
print(f"б) (A%2==0) ^ (B%2==0)  →  ({A}%2==0) ^ ({B}%2==0)  →  {A%2==0} ^ {B%2==0}  =  {result_b}")

# в) Хотя бы одно из чисел А и В положительно
# «Хотя бы одно» — достаточно одного True, используем «или» (or)
# A=150 > 0 → True; True or ... = True (не важно что B)
result_c = A > 0 or B > 0
print(f"в) A > 0 or B > 0  →  {A} > 0 or {B} > 0  →  {A > 0} or {B > 0}  =  {result_c}")

# г) Каждое из чисел А, В, С кратно трём
# Кратность трём: n % 3 == 0; все три должны быть кратны → and
# 150%3=0 True; 42%3=0 True; 99%3=0 True; True and True and True = True
result_g = A % 3 == 0 and B % 3 == 0 and C % 3 == 0
print(f"г) A%3==0 and B%3==0 and C%3==0  →  {A%3==0} and {B%3==0} and {C%3==0}  =  {result_g}")

# д) Только одно из чисел А, В, С меньше 50
# «Только одно» из трёх — это сумма булевых значений равна 1
# (True считается как 1, False как 0)
# A=150 < 50 → False(0); B=42 < 50 → True(1); C=99 < 50 → False(0); сумма=1 → True
count_less_50 = (A < 50) + (B < 50) + (C < 50)
result_d = count_less_50 == 1
print(f"д) ровно одно из (A<50, B<50, C<50)  →  {A<50} + {B<50} + {C<50} = {count_less_50}  →  count==1  =  {result_d}")

# е) Хотя бы одно из чисел А, В, С отрицательно
# «Хотя бы одно» → or
result_e = A < 0 or B < 0 or C < 0
print(f"е) A<0 or B<0 or C<0  →  {A<0} or {B<0} or {C<0}  =  {result_e}")


