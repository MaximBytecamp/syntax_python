# Задание 1.11 (часть 2)
# Составить программу обмена значениями двух переменных.
# ==============================================================

x = 10
y = 25

print(f"\nДо обмена:  x = {x}, y = {y}")

# Обмен значениями через третью переменную (временную)
temp = x
x = y
y = temp

print(f"После обмена: x = {x}, y = {y}")

# Альтернативный способ обмена — «питонический»: одной строкой
a = 100
b = 200
print(f"\nДо обмена (питонический):  a = {a}, b = {b}")
a, b = b, a  # Python позволяет менять значения без временной переменной
print(f"После обмена (питонический): a = {a}, b = {b}")


# ==============================================================
# Задание 1.12
# Составить программу обмена значениями трёх переменных a, b, c:
# а) b ← c, a ← b, c ← a  (сдвиг: b получает c, a получает b, c получает a)
# б) b ← a, c ← b, a ← c  (сдвиг в другую сторону)
# ==============================================================

# --- Вариант а) ---
a = 1
b = 2
c = 3

print(f"\nВариант а). До:    a={a}, b={b}, c={c}")

# Важно! Если делать по одному, значения перезаписываются,
# поэтому используем одновременное присваивание через кортеж
a, b, c = b, c, a  # Python сначала вычисляет правую часть, потом присваивает

print(f"Вариант а). После: a={a}, b={b}, c={c}")
# Ожидаем: a=2, b=3, c=1 (каждый получил значение следующего: b→a, c→b, a→c)

# --- Вариант б) ---
a = 1
b = 2
c = 3

print(f"\nВариант б). До:    a={a}, b={b}, c={c}")

a, b, c = c, a, b  # сдвиг в противоположную сторону

print(f"Вариант б). После: a={a}, b={b}, c={c}")
# Ожидаем: a=3, b=1, c=2 (каждый получил значение предыдущего: c→a, a→b, b→c)
