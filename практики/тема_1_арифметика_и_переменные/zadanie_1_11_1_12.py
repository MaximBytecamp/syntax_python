# Задание 1.11 (часть 1)
# Дана сторона квадрата. Найти его периметр.
# Дан радиус окружности. Найти её диаметр.
# Результат вычислений вывести на экран.
# ==============================================================

side = 5        # сторона квадрата
radius = 7      # радиус окружности

# Периметр квадрата = 4 * сторона
perimeter = 4 * side
print(f"Сторона квадрата: {side}")
print(f"Периметр квадрата: {perimeter}")

# Диаметр окружности = 2 * радиус
diameter = 2 * radius
print(f"Радиус окружности: {radius}")
print(f"Диаметр окружности: {diameter}")


