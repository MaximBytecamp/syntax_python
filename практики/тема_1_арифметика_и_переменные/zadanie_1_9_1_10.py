import math

# ==============================================================
# Задание 1.9
# Даны два целых числа. Найти:
# а) их среднее арифметическое;
# б) их среднее геометрическое.
# ==============================================================

a = 4
b = 16

# а) Среднее арифметическое — сумма делённая на количество чисел
arithmetic_mean = (a + b) / 2
print(f"Среднее арифметическое {a} и {b}: {arithmetic_mean}")

# б) Среднее геометрическое — корень квадратный из произведения чисел
# math.sqrt() вычисляет квадратный корень
geometric_mean = math.sqrt(a * b)
print(f"Среднее геометрическое {a} и {b}: {geometric_mean}")


# ==============================================================
# Задание 1.10
# Известно значение температуры по шкале Цельсия. Найти
# соответствующее значение температуры по шкале:
# а) Фаренгейта  — формула: F = C * 1.8 + 32
# б) Кельвина    — формула: K = C + 273.15
# ==============================================================

celsius = 36.6  # температура в градусах Цельсия

# а) Перевод в Фаренгейты: умножаем на 1.8 и прибавляем 32
fahrenheit = celsius * 1.8 + 32
print(f"{celsius}°C = {fahrenheit:.2f}°F (Фаренгейт)")

# б) Перевод в Кельвины: прибавляем абсолютный ноль (273.15)
kelvin = celsius + 273.15
print(f"{celsius}°C = {kelvin:.2f} K (Кельвин)")
