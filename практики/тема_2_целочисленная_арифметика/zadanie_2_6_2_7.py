# ==============================================================
# Задание 2.6
# Дано двузначное число. Найти:
# а) число десятков в нём;
# б) число единиц в нём;
# в) сумму его цифр;
# г) произведение его цифр.
# ==============================================================

number = 47  # двузначное число

# а) Цифра десятков — целое частное от деления на 10
tens = number // 10
print(f"Число: {number}")
print(f"а) Цифра десятков: {tens}")

# б) Цифра единиц — остаток от деления на 10
units = number % 10
print(f"б) Цифра единиц: {units}")

# в) Сумма цифр
digit_sum = tens + units
print(f"в) Сумма цифр: {digit_sum}")

# г) Произведение цифр
digit_product = tens * units
print(f"г) Произведение цифр: {digit_product}")


# ==============================================================
# Задание 2.7
# Дано двузначное число. Получить число, образованное при
# перестановке цифр заданного числа.
# Например: 47 → 74
# ==============================================================

number = 47  # двузначное число

tens = number // 10   # цифра десятков
units = number % 10   # цифра единиц

# Меняем цифры местами: бывшие единицы становятся десятками и наоборот
# Новое число = (старые единицы) * 10 + (старые десятки)
reversed_number = units * 10 + tens

print(f"\nИсходное число: {number}")
print(f"Число с переставленными цифрами: {reversed_number}")
