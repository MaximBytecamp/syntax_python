# ==============================================================
# Задание 2.3
# С некоторого момента прошло 234 дня. Сколько полных недель
# прошло за этот период?
# ==============================================================

days = 234  # количество прошедших дней

# // — оператор целочисленного деления (возвращает целое число без остатка)
# % — оператор взятия остатка от деления (модуль)
full_weeks = days // 7          # сколько полных недель
remaining_days = days % 7       # сколько дней осталось после полных недель

print(f"Прошло дней: {days}")
print(f"Полных недель: {full_weeks}")
print(f"Остаток дней (сверх полных недель): {remaining_days}")


# ==============================================================
# Задание 2.4
# С начала суток прошло n секунд. Определить:
# а) сколько полных часов прошло с начала суток;
# б) сколько полных минут прошло с начала очередного часа;
# в) сколько полных секунд прошло с начала очередной минуты.
# ==============================================================

n = 75_643  # секунд прошло с начала суток (нижнее подчёркивание — разделитель для читаемости)

# а) Полные часы с начала суток
# В одном часе 3600 секунд (60 мин * 60 сек)
hours = n // 3600
print(f"\nС начала суток прошло секунд: {n}")
print(f"а) Полных часов с начала суток: {hours}")

# б) Полные минуты с начала текущего (очередного) часа
# Сначала находим остаток секунд после вычета полных часов
seconds_after_hours = n % 3600   # секунды, «не вошедшие» в полные часы
minutes = seconds_after_hours // 60  # переводим в минуты (целое деление на 60)
print(f"б) Полных минут с начала очередного часа: {minutes}")

# в) Полные секунды с начала текущей (очередной) минуты
# Берём остаток после вычета полных минут
seconds = seconds_after_hours % 60
print(f"в) Полных секунд с начала очередной минуты: {seconds}")

# Контрольный вывод в формате ЧЧ:ММ:СС
print(f"\nИтог: {hours:02d}:{minutes:02d}:{seconds:02d}")
