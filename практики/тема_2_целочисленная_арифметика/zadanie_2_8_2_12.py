# Задание 2.9
# Дано трёхзначное число. Найти число, полученное при прочтении
# его цифр справа налево.
# Например: 375 → 573
# ==============================================================

number = 375  # трёхзначное число

# Извлекаем цифры
hundreds = number // 100        # 3
tens     = (number % 100) // 10 # 7
units    = number % 10          # 5

# Собираем число в обратном порядке:
# бывшие единицы → сотни, десятки → десятки (остаются), сотни → единицы
reversed_number = units * 100 + tens * 10 + hundreds

print(f"\nИсходное число: {number}")
print(f"Число, прочитанное справа налево: {reversed_number}")


# ==============================================================
# Задание 2.10
# Дано трёхзначное число. В нём зачеркнули первую слева цифру
# и приписали её в конце.
# Например: 375 → 753
# ==============================================================

number = 375  # трёхзначное число

hundreds = number // 100         # первая (левая) цифра — 3
rest     = number % 100          # оставшиеся две цифры — 75

# Сдвигаем rest влево на один разряд (умножаем на 10)
# и дописываем в конец вычеркнутую сотню
result = rest * 10 + hundreds

print(f"\nИсходное число: {number}")
print(f"После перестановки первой цифры в конец: {result}")


# ==============================================================
# Задание 2.11
# Дано трёхзначное число. В нём зачеркнули последнюю справа
# цифру и приписали её в начале.
# Например: 375 → 537
# ==============================================================

number = 375  # трёхзначное число

units = number % 10      # последняя (правая) цифра — 5
rest  = number // 10     # оставшиеся две цифры — 37

# Ставим вычеркнутые единицы в начало (на место сотен)
# и приписываем rest после них
result = units * 100 + rest

print(f"\nИсходное число: {number}")
print(f"После перестановки последней цифры в начало: {result}")


# ==============================================================
# Задание 2.12
# Дано трёхзначное число. Найти число, полученное при
# перестановке первой и второй цифр.
# Например: 375 → 735
# ==============================================================

number = 375  # трёхзначное число

hundreds = number // 100          # цифра сотен  — 3
tens     = (number % 100) // 10   # цифра десятков — 7
units    = number % 10            # цифра единиц — 5

# Меняем местами hundreds и tens, units остаётся на месте
result = tens * 100 + hundreds * 10 + units

print(f"\nИсходное число: {number}")
print(f"После перестановки 1-й и 2-й цифр: {result}")
