# ==============================================================
# Задание 2.8
# Дано трёхзначное число. Найти:
# а) число единиц в нём;
# б) число десятков в нём;
# в) сумму его цифр;
# г) произведение его цифр.
# ==============================================================

number = 375  # трёхзначное число

# а) Цифра единиц — остаток от деления на 10
units = number % 10
print(f"Число: {number}")
print(f"а) Цифра единиц: {units}")

# б) Цифра десятков — берём последние две цифры (% 100),
#    затем убираем единицы (// 10)
tens = (number % 100) // 10
print(f"б) Цифра десятков: {tens}")

# в) Цифра сотен — целое частное от деления на 100
hundreds = number // 100
print(f"   Цифра сотен: {hundreds}")

# г) Сумма цифр
digit_sum = hundreds + tens + units
print(f"в) Сумма цифр: {digit_sum}")

# д) Произведение цифр
digit_product = hundreds * tens * units
print(f"г) Произведение цифр: {digit_product}")


