# Задание 11.16
# Дан массив целых чисел. Выяснить:
# а) является ли s-й элемент массива положительным числом;
# б) является ли k-й элемент массива чётным числом;
# в) какой элемент массива больше: k-й или s-й.
# ==============================================================

def zadanie_11_16():
    lst = create_lst()
    print(f"Массив: {lst}")

    s = int(input(f"Введите индекс s (0..{len(lst)-1}): "))
    k = int(input(f"Введите индекс k (0..{len(lst)-1}): "))

    # а) Положительный ли s-й элемент?
    print(f"а) lst[{s}] = {lst[s]}, положительный: {lst[s] > 0}")

    # б) Чётный ли k-й элемент?
    print(f"б) lst[{k}] = {lst[k]}, чётный: {lst[k] % 2 == 0}")

    # в) Какой из двух элементов больше?
    if lst[k] > lst[s]:
        print(f"в) lst[{k}] = {lst[k]} больше lst[{s}] = {lst[s]}")
    elif lst[s] > lst[k]:
        print(f"в) lst[{s}] = {lst[s]} больше lst[{k}] = {lst[k]}")
    else:
        print(f"в) lst[{k}] и lst[{s}] равны: {lst[k]}")

