# --- Копирование списков ---

lst = [1, 2, [3, 4]]

# Поверхностное копирование (shallow copy):
# изменения во вложенных объектах затрагивают обе переменные
lst2 = lst.copy()
lst[0] = 99
print(f"shallow copy: lst={lst}, lst2={lst2}")   # lst2[0] не изменился

lst[2][0] = 56   # но вложенный список — общий!
print(f"вложенный: lst={lst}, lst2={lst2}")      # lst2[2][0] тоже изменился

# Глубокое копирование (deep copy): полностью независимая копия
lst = [1, 2, [3, 4]]
lst2 = deepcopy(lst)
lst[2][0] = 56
print(f"deep copy: lst={lst}, lst2={lst2}")      # lst2[2][0] НЕ изменился


