# --- Полное решение задачи 2.6 через отдельные функции ---

def count_tens(num):
    """Возвращает цифру десятков двузначного числа."""
    return num // 10

def count_units(num):
    """Возвращает цифру единиц двузначного числа."""
    return num % 10

def sum_digits(num):
    """Возвращает сумму цифр двузначного числа."""
    return count_tens(num) + count_units(num)  # вызов одних функций внутри другой

def multiply_digits(num):
    """Возвращает произведение цифр двузначного числа."""
    return count_tens(num) * count_units(num)

def zadanie_2_6():
    num = int(input("Введите двузначное число: "))

    print(f"а) Цифра десятков:    {count_tens(num)}")
    print(f"б) Цифра единиц:      {count_units(num)}")
    print(f"в) Сумма цифр:        {sum_digits(num)}")
    print(f"г) Произведение цифр: {multiply_digits(num)}")

