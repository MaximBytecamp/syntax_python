# Задание 11.17
# Дан массив. Все его элементы:
# а) увеличить в 2 раза;
# б) уменьшить на число A;
# в) разделить на первый элемент.
# ==============================================================

def zadanie_11_17():
    lst = create_lst()
    print(f"Исходный массив: {lst}")

    # а) Умножить каждый элемент на 2
    lst_a = [x * 2 for x in lst]
    print(f"а) × 2:              {lst_a}")

    # б) Уменьшить каждый элемент на A
    A = int(input("Введите число A: "))
    lst_b = [x - A for x in lst]
    print(f"б) − A ({A}):         {lst_b}")

    # в) Разделить на первый элемент (если он не равен 0)
    if lst[0] == 0:
        print("в) Первый элемент равен 0, деление невозможно")
    else:
        lst_c = [x / lst[0] for x in lst]
        print(f"в) ÷ lst[0] ({lst[0]}): {[round(v, 2) for v in lst_c]}")

zadanie_11_17()
