# Задание 11.15
# Дан массив. Составить программу:
# а) расчёта квадратного корня из любого элемента массива;
# б) расчёта среднего арифметического двух любых элементов массива.
# ==============================================================

# --- Вариант с передачей списка через параметр (правильный стиль) ---
# Глобальные переменные (global) использовать не рекомендуется,
# лучше передавать список как аргумент функции.

def create_lst(n=10):
    """Создаёт список из n случайных чисел от -100 до 100."""
    return [randint(-100, 100) for _ in range(n)]

def square_root_of_element(lst):
    """
    а) Просит ввести элемент, проверяет его наличие и знак,
    возвращает квадратный корень или сообщение об ошибке.
    """
    num = int(input("Введите элемент, из которого берём корень: "))

    if lst.count(num) == 0:
        return "Такого элемента в массиве нет"
    if num < 0:
        return "Корень из отрицательного числа взять нельзя"

    return math.sqrt(num)   # или num ** (1/2)

def mean_of_two(lst):
    """
    б) Просит ввести два индекса, возвращает среднее арифметическое
    двух элементов массива.
    """
    i = int(input(f"Введите первый индекс (0..{len(lst)-1}): "))
    j = int(input(f"Введите второй индекс (0..{len(lst)-1}): "))
    return (lst[i] + lst[j]) / 2

def zadanie_11_15():
    lst = create_lst()
    print(f"Массив: {lst}")

    print(f"а) Корень: {square_root_of_element(lst)}")
    print(f"б) Среднее двух элементов: {mean_of_two(lst)}")

